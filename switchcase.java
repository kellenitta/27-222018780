 public class switchcase {  
        public static void main(String[] args) {  
            int weight=40;  
            switch(weight){   
            case 40: System.out.println("approved");  
            break;  
            case 50: System.out.println("approved");  
            break;  
            case 60: System.out.println("approved");  
            break;  
            default:System.out.println("Not approved");  
            }  
        }  
        }  
